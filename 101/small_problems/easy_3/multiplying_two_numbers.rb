# Multiplying Two Numbers

# Create a method that takes two arguments, multiplies them together, 
# and returns the result.

# understanding
# input
#   two ints
# output
#   returns the two ints multiplied together

def multiply(int1, int2)
    int1 * int2
end

# tests
puts multiply(5, 3) == 15

# further exploration
p multiply(['2', '3'], 2)
