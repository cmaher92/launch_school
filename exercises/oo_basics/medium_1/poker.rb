class PokerHand
  def initialize(deck)
    @hand = []
    5.times { @hand << deck.draw }
  end

  def print
    puts @hand
  end

  def evaluate
    case
    when royal_flush?     then 'Royal flush'
    when straight_flush?  then 'Straight flush'
    when four_of_a_kind?  then 'Four of a kind'
    when full_house?      then 'Full house'
    when flush?           then 'Flush'
    when straight?        then 'Straight'
    when three_of_a_kind? then 'Three of a kind'
    when two_pair?        then 'Two pair'
    when pair?            then 'Pair'
    else                       'High card'
    end
  end

  private

  def royal_flush?
    flush? && @hand.sort.map(&:rank) == [10, "Jack", "Queen", "King", "Ace"]
  end

  def straight_flush?
    flush? && straight?
  end

  def four_of_a_kind?
    @hand.any? { |card| @hand.count(card) == 4 }
  end

  def full_house?
    three_of_a_kind? &&
    @hand.reject { |card| @hand.count(card) == 3 }.map(&:value).uniq.size == 1
  end

  def flush?
    @hand.all? { |card| card.suit == @hand.first.suit }
  end

  def straight?
    values = @hand.sort.map(&:value)
    values == Array.new(5) { |index| values.first + index }
  end

  def three_of_a_kind?
    @hand.any? { |card| @hand.count(card) == 3 }
  end

  def two_pair?
    @hand.select { |card| @hand.count(card) == 2 }.map(&:value).uniq.size == 2
  end

  def pair?
    @hand.any? { |card| @hand.count(card) == 2 }
  end
end

