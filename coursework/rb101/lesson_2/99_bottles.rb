# Write a program that prints out the lyrics to 99 bottles of beer on the wall

# Informal psuedo-code
bottle_count = 99

while the bottle_count is above or equal to 0
  - print the chorus to 99 bottles
  - subtract one bottle from the bottle_count
  
# Formal psuedo-code
START

SET bottle_count = 99

WHILE bottle_count > 0
  PRINT bottle_count and the chorus
  SET   bottle_count to be one less bottle
  
 
END